#include "libft.h"

int	ft_atoi(const char *s)
{
	long			ret;
	long			sign;
	unsigned int	i;

	ret = 0;
	sign = 1;
	i = 0;
	while (s[i] == ' ' || s[i] == '\n' || s[i] == '\f' ||
			s[i] == '\t' || s[i] == '\v' || s[i] == '\r')
		i++;
	if (s[i] == '-' || s[i] == '+')
	{
		if (s[i] == '-')
			sign = -1;
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9' && s[i])
	{
		ret = ret * 10 + s[i] - '0';
		i++;
	}
	return (ret * sign);
}
